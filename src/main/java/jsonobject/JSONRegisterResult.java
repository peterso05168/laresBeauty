package jsonobject;

public class JSONRegisterResult {
	private String code;
	private String detail;
	private JSONRegister data;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public JSONRegister getData() {
		return data;
	}

	public void setData(JSONRegister data) {
		this.data = data;
	}

}